% --- Executes just before untitled is made visible.
function untitled_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to untitled (see VARARGIN)

% Choose default command line output for untitled
    handles.output = hObject;
    image = imread('../images/Captura.PNG');
    axes(handles.axes2);
    
    imshow(image);    
    [rows, cols, dim] = size(image);
    
    set(handles.axes2,'UserData',image);
    
    maxWindowSize = round(min(rows, cols)/5);
    minWindowSize = maxWindowSize/1000;
    currentWindowSize = maxWindowSize/10;
    
    set(handles.slider1,'Min', round(currentWindowSize/2));
    set(handles.slider2,'Min', round(currentWindowSize/2));
    
    set(handles.slider1,'Max', cols- round(currentWindowSize/2));
    set(handles.slider2,'Max', rows - round(currentWindowSize/2));
    
    set(handles.slider1,'Value', round(cols/2));
    set(handles.slider2,'Value', round(cols/2));
    
    
    set(handles.slider4,'Max', maxWindowSize);
    set(handles.slider4,'Min', minWindowSize);
    set(handles.slider4,'Value', currentWindowSize);
    
    handles.xdata = 0;
    handles.ydata = 0;


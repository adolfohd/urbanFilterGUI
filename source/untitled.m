% --- Executes on slider movement.
function slider_Callback(hObject, eventdata, handles)
% hObject    handle to slider1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Hints: get(hObject,'Value') returns position of slider
%        get(hObject,'Min') and get(hObject,'Max') to determine range of slider

image = get(handles.axes2,'UserData');
[row,col, dim] = size(image) 
xpos =  round(get(handles.slider1,'Value'))
ypos = -round(get(handles.slider2,'Value'))+row+1

windowSize = round(get(handles.slider4,'Value'))
set(handles.edit1,'String', windowSize)
axes(handles.axes1)

xmin = round(xpos - windowSize/2)
xmax = round(xpos + windowSize/2)
ymin = round(ypos - windowSize/2)
ymax = round(ypos + windowSize/2)

if (xmin < 1)
    xmin = 1
    xmax = windowSize  
end


if (xmax > col)
    xmax = col
    xmin = col-windowSize   
end


if (ymin < 1)
    ymin = 1
    ymax = windowSize   
end
if (ymax > row)
    ymax = row
    ymin = row - windowSize   
end


imshow(image(ymin:ymax, xmin:xmax, :))

handles.xmin = xmin;
handles.ymin = ymin;

guidata(hObject,handles)


